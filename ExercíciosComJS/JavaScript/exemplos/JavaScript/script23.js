function soma(numero1,numero2){
    return alert(`A soma dos valores é ${numero1} + ${numero2} = ${numero1+numero2}`);
}

function subtracao(numero1,numero2){
    return alert(`A subtração dos valores é ${numero1} - ${numero2} = ${numero1-numero2}`);
}

function multi(numero1,numero2){
   
    return alert(`A multiplicação dos valores é ${numero1} x ${numero2} = ${numero1*numero2}`);
}
function div(numero1,numero2){
    return alert(`A divisão dos valores é ${numero1} / ${numero2} = ${numero1/numero2}`);
}